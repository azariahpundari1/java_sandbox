public class Main {
    public static void main(String[] args) {

      // the println() method - prints a new line with every statment
      System.out.println("Println() examples");
      System.out.println("LOL");
      System.out.println("Hi");

      // the print() method - notice it does not print a new line after each statememnt
      System.out.println("Print() examples");
      System.out.print("Hello World! ");
      System.out.print("This will be on the same line");
      System.out.println("");

      // printing numerical values
      System.out.println("Print numbers example: ");
      System.out.println(330);
      System.out.println(201);

      // math calculations using print
      System.out.println("Math calculations using println()");
      System.out.println(3 + 3);
      System.out.println(4 + 6);
    }
  }